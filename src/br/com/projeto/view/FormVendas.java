    private void btnPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagamentoActionPerformed
        // Funcionamento do botao pagamento;
        String nome = txtNome.getText();
        String cep = txtCep.getText();
        Cliente obj = new Cliente();
        ClienteDAO dao = new ClienteDAO();
        obj = dao.buscarCliente(nome);
        obj= dao.buscarClienteCEP(cep);
        if(obj.getNome() != null && obj.getCep() != null){
            FormPagamentos formpagamentos = new FormPagamentos();
            formpagamentos.txtTotal.setText(String.valueOf(total));
            formpagamentos.cliente = obj;
            formpagamentos.carrinho = carrinho;
            formpagamentos.setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnPagamentoActionPerformed

