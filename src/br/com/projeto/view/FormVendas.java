    private void btnaddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddItemActionPerformed
        // add no carrinho;
        String nome = txtProduto.getText();
        Produto obj = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        obj = dao.BuscarProdutoNome(nome);
        if(obj.getDescricao() != null){
            int quantidade = Integer.valueOf(txtQuantidade.getText());
            int estoque = Integer.valueOf(txtEstoque.getText());
            preco = Double.valueOf(txtPreco.getText());
            qtd = Integer.valueOf(txtQuantidade.getText());
            subtotal = preco * qtd;
            total += subtotal;
            if(estoque>=quantidade){
                txtTotalVenda.setText(String.valueOf(total));
               //Att: na tabela do carrinho os dados da tabela devem continuar no tipo object;
                carrinho = (DefaultTableModel) tabelaCarrinho.getModel();
                carrinho.addRow(new Object[]{
                txtCodigo.getText(),
                txtProduto.getText(),
                txtQuantidade.getText(),
                txtPreco.getText(),
                subtotal,
                });               
            }
            else{
              JOptionPane.showMessageDialog(null, "Quantidade indisponivel no estoque");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Faltam dados a serem informados, veridique o Nome, e"
                    + " outros campos");
        }
        
        
    }//GEN-LAST:event_btnaddItemActionPerformed

    
