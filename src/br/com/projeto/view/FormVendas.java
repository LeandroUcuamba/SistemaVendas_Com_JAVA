    private void txtPesqProdutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesqProdutoKeyReleased
         // Pesquisar produto por nome;
       String nome = "%"+txtPesqProduto.getText()+"%";
       ProdutoDAO dao = new ProdutoDAO();
       List<Produto> lista = dao.PesquisarNome(nome);
          DefaultTableModel dados = (DefaultTableModel) tabelaProdVenda.getModel();
          dados.setNumRows(0);
          for(Produto c: lista){
              dados.addRow(new Object[]{
                 c.getId(),
                 c.getDescricao(),
                 c.getPreco(),
                 c.getQuantidade(),
                 c.getFornecedor().getNome()
              });
              
          }

    }//GEN-LAST:event_txtPesqProdutoKeyReleased

