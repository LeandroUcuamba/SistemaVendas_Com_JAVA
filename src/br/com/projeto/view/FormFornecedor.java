    public void listar(){
       FornecedorDAO dao = new FornecedorDAO();
       List<Fornecedor> lista = dao.listarFornecedores();
          DefaultTableModel dados = (DefaultTableModel) tabelaFornecedor.getModel();
          dados.setNumRows(0);
          for(Fornecedor c: lista){
              dados.addRow(new Object[]{
                 c.getId(),
                 c.getNome(),
                 c.getEmail(),
                 c.getTelefone(),
                 c.getEndereco(),
                 c.getNumero(),
                 c.getComplemento(),
                 c.getEstado(),
              });
              
          }
    
    }
    
