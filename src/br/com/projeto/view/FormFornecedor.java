    private void txtPesquisarClienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarClienteKeyReleased
       // Pesquisar cliente por nome;
       String nome = "%"+txtPesquisarCliente.getText()+"%";
       FornecedorDAO dao = new FornecedorDAO();
       List<Fornecedor> lista = dao.PesquisarNome(nome);
          DefaultTableModel dados = (DefaultTableModel) tabelaFornecedor.getModel();
          dados.setNumRows(0);
          for(Fornecedor c: lista){
              dados.addRow(new Object[]{
                 c.getId(),
                 c.getNome(),
                 c.getEmail(),
                 c.getTelefone(),
                 c.getEndereco(),
                 c.getNumero(),
                 c.getComplemento(),
                 c.getEstado()
              });
              
          }
    }//GEN-LAST:event_txtPesquisarClienteKeyReleased

