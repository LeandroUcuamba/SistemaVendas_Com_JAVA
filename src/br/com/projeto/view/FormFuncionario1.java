    private void btnPesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqActionPerformed
        //Pesquisar por nome;
        String nome = txtNome.getText();
        Funcionario obj= new Funcionario();
        FuncionarioDAO dao = new FuncionarioDAO();
        obj = dao.buscarFuncionario(nome);

        if(obj.getNome() != null){

            txtCodigo.setText(String.valueOf(obj.getId()));
            txtNome.setText(obj.getNome());
            txtEmail.setText(obj.getEmail());
            txtSenha.setText(obj.getSenha());
            txtCargo.setText(obj.getCargo());
            cbNivelAcesso.setSelectedItem(obj.getNivelAcesso());
            txtTelefone.setText(obj.getTelefone());
            txtCEP.setText(obj.getCep());
            txtEndereco.setText(obj.getEndereco());
            txtNumero.setText(obj.getNumero());
            txtComplemento.setText(obj.getComplemento());
            cbUF.setSelectedItem(obj.getEstado());

        }else{
            JOptionPane.showMessageDialog(null, "Funcionario nao encontrado...");
        }

    }//GEN-LAST:event_btnPesqActionPerformed

