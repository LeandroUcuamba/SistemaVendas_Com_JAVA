    private void txtPesquisarFuncionarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarFuncionarioKeyReleased
       // Pesquisar cliente por nome;
       String nome = "%"+txtPesquisarFuncionario.getText()+"%";
       FuncionarioDAO dao = new FuncionarioDAO();
       List<Funcionario> lista = dao.PesquisarNome(nome);
          DefaultTableModel dados = (DefaultTableModel) tabelaFuncionario.getModel();
          dados.setNumRows(0);
          for(Funcionario c: lista){
              dados.addRow(new Object[]{
                 c.getId(),
                 c.getNome(),
                 c.getEmail(),
                 c.getSenha(),
                 c.getCargo(),
                 c.getNivelAcesso(),
                 c.getTelefone(),
                 c.getCep(),
                 c.getEndereco(),
                 c.getNumero(),
                 c.getComplemento(),
                 c.getEstado()
              });
              
          }
    }//GEN-LAST:event_txtPesquisarFuncionarioKeyReleased

