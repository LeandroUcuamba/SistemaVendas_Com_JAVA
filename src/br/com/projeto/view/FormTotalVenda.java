    private void btnPesquisar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisar2ActionPerformed
        //Bot√£o somatorio do dia;
        try{
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data = LocalDate.parse(txtData.getText(), formato);
            double totalVenda;
            VendaDAO dao = new VendaDAO();
            totalVenda = dao.totalVendas(data);
            txtTotal.setText(String.valueOf(totalVenda));
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro " + e);
        }
        
    }//GEN-LAST:event_btnPesquisar2ActionPerformed

